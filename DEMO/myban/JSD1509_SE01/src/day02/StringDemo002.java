package day02;
/**
 *  包装类提供了两个常量
 *  MAX_VALUE:最大值
 *  MIN_VALUE:最小值
 * @author Administrator
 *
 */
public class StringDemo002 {
	public static void main(String[] args) {
		int max = Integer.MAX_VALUE;
		System.out.println(max);//2147483647
		
		int imin = Integer.MIN_VALUE;
		System.out.println(imin);//-2147483648
	}
}










